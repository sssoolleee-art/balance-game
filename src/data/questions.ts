// Seeded percentage — stays consistent per question
// Skewed toward 30–70 range to feel realistic
export function getSeededPercent(id: number): number {
  const x = Math.sin(id * 127.1 + 311.7) * 43758.5453;
  const raw = x - Math.floor(x); // 0~1
  return Math.round(30 + raw * 40); // 30~70
}
